# En python los datos en la memoria tienen distintos tipos
# Porque no todas las instrucciones son compatibles con todos los datos
# Ademas, distintos datos requieren mas o menos espacio en la memoria

# En python los tipos de datos mas sencillos son:
#   Los bool (booleanos, verdadero o falso:
bool_true = True
bool_false = False


#   Los numeros pueden ser enteros (int) o de punto flotante (float)
int_num = 128
float_num = 128.256


# Despues tenemos las secuencias, que son colleciones de datos, no solo uno
# Los string o cadenas de caracteres
string_var = 'This is a string'

# Tambien tenemos las tuplas o las listas
tuple_var = (1, 2, 3)
list_var = [1, 2, 3, 4]


# por ultimo existen los sets y los diccionarios
set_var = {1, 2, 3}
dictionary = {'first': 1, 'second': 2, 'third': 3}


