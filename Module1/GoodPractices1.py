# Si queremos que nuestro proceso de desarrollo de software sea lo mas sencillo y efectivo posible
# Existe una serie de consejos y recomendaciones que podemos seguir, llamados mejores practicas
# De hecho, si queremos un trabajo desarrollando software, es muy importante seguirlas.


# La primera buena practica que podemos mencionar habla de los nombres de las variables
# Cada lenguaje tiene ciertas reglas para el nombre de las variables, en general son:
# Usar letras, numeros, o guion bajo, pero no comenzar con un numero
# No puede haber espacios y otros simbolos
# Debe ser un nombre unico, no se pueden usar palabras propias del lenguaje (como 'print')

# Ademas de eso, las mejores practicas nos dan algunas recomendaciones mas
# Por ejemplo, poner nombres descriptivos, evitar cosas como 'x' o 'y'
# Mientras la variable de use en mas lugares, mas descriptivo debe ser el nombre
# Escoger un estilo y mantenerlo en todo el programa, ejemplos de estilos pueden ser:
# variable_name -> separar palabras con guion bajo
# variableName -> separar palabras con una mayuscula (se le llama 'camelCase')
# CONSTANT -> usar mayusculas para datos constantes, que no vayan a cambiar en el programa
# is_valid -> usar lenguaje descriptivo, aqui un ejemplo de una variable booleana (true o false)
