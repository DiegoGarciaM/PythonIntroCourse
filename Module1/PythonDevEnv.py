# Existen varias maneras para que una computadora ejecute el contenido de un archivo
# Dependiendo del lenguaje de programacion en el que esta escrito el proceso es diferente

# Para muchos lenguajes como C, hay que compilar el codigo
# Eso significa traducirlo para la computadora a un archivo que pueda ejecutar

# Para lenguajes como python, el codigo no es compilado, es interpretado
# Es decir, hay un programa intermedio que se encarga de leer nuestro codigo
# Y traducirlo en tiempo real para que la computadora lo ejecute

# Programar en python es tan simple como descargar e instalar ese interprete (llamado python)
# Luego podemos escribir nuestros scripts, y darselos para que los ejecute.

# Existen ambientes de programacion que lo hacen sin que nos demos cuenta,
# Pero vale la pena aprender a hacerlo manualmente, ya que no es tan dificil
